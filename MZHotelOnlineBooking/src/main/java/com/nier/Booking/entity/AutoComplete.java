package com.nier.Booking.entity;

public class AutoComplete {
		private int autoCompleteId;		//自动补全的ID
		private String autoAddress;		//自动补全的地址
		public int getAutoCompleteId() {
			return autoCompleteId;
		}
		public void setAutoCompleteId(int autoCompleteId) {
			this.autoCompleteId = autoCompleteId;
		}
		public String getAutoAddress() {
			return autoAddress;
		}
		public void setAutoAddress(String autoAddress) {
			this.autoAddress = autoAddress;
		}
		
}
